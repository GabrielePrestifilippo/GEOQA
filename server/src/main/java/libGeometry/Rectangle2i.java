package libGeometry;

public class Rectangle2i {

    private int xLowLeft, yLowLeft, xUpRight, yUpRight;

    public Rectangle2i() {
    }

    public int getxLowLeft() {
        return xLowLeft;
    }

    public void setxLowLeft(int xLowLeft) {
        this.xLowLeft = xLowLeft;
    }

    public int getyLowLeft() {
        return yLowLeft;
    }

    public void setyLowLeft(int yLowLeft) {
        this.yLowLeft = yLowLeft;
    }

    public int getxUpRight() {
        return xUpRight;
    }

    public void setxUpRight(int xUpRight) {
        this.xUpRight = xUpRight;
    }

    public int getyUpRight() {
        return yUpRight;
    }

    public void setyUpRight(int yUpRight) {
        this.yUpRight = yUpRight;
    }

}
